# -*- coding: utf-8 -*-
"""
Пример использования парсера клинических протоколов.
"""

from parser import ClinicalProtocolParser
from search_engine import ClinicalSearchEngine

# Демо-данные из извлечённого протокола
DEMO_TEXT = """
# Клинический протокол

## Наджелудочковая тахикардия (I47.1)

### Диагностика

**Обязательная:**
- Биохимическое исследование крови: определение концентрации калия, магния, холестерина
- ЭКГ в 12-ти отведениях
- Консультация врача-кардиолога
- Холтеровское мониторирование (ХМ)
- Эхо-КГ

**Дополнительная (по показаниям):**
- ЧПЭФИ
- ФГДС
- Гормоны щитовидной железы (ТТГ, Т4 свободный)
- Липидограмма

### Купирование приступа

1. Вагусные приемы
2. Верапамил 0,25% раствор 2-4 мл (5-10 мг) внутривенно медленно
3. Метопролол 0,1% раствор - первоначально 2,5-5 мл (2,5-5,0 мг) внутривенно, при необходимости повторить до 20 мг
4. Прокаинамид 500-1000 мг внутривенно капельно
5. Пропафенон 0,5-1 мг/кг внутривенно за 10-20 минут
6. Трифосаденин 1% раствор 1-2 мл (10-20 мг) внутривенно (противопоказан при остром коронарном синдроме и бронхиальной астме)
7. Аденозин 6 мг внутривенно болюсом, при неэффективности - 12 мг (противопоказан при ОКС и бронхиальной астме)
8. Амиодарон 300 мг внутривенно при неэффективности
9. Электроимпульсная терапия (ЭИТ) 50-360 Дж

### Профилактика приступов

1. Метопролол 25-200 мг/сут
2. Бисопролол 2,5-10 мг/сут
3. Соталол 80-320 мг/сут (под контролем QT)
4. Пропафенон 450-600 мг/сут (при отсутствии структурной патологии сердца)
5. Этацизин 150-200 мг/сут
6. Верапамил 120-360 мг/сут
7. Амиодарон 100-400 мг/сут (при неэффективности, требует контроля функции щитовидной железы, печени, легких)
"""


def main():
    print("=" * 60)
    print("Демонстрация парсера клинических протоколов")
    print("=" * 60)

    # Инициализация парсера
    parser = ClinicalProtocolParser()

    # Парсинг из текста
    print("\n[1] Парсинг демо-текста...")
    chunks = parser.parse_from_text(DEMO_TEXT, "demo_protocol")
    print(f"    Извлечено фрагментов: {len(chunks)}")

    # Показать первый chunk
    if chunks:
        first = chunks[0]
        print(f"\n[2] Пример фрагмента:")
        print(f"    ID: {first.chunk_id}")
        print(f"    Диагноз: {first.diagnosis}")
        print(f"    МКБ: {first.icd_code}")
        print(f"    Раздел: {first.section_type}")
        print(f"    Препараты: {[m.get('name') for m in first.medications]}")

    # Экспорт в JSON
    json_path = "/workspace/clinical_parser/demo_output.json"
    parser.to_json(chunks, json_path)
    print(f"\n[3] JSON сохранён: {json_path}")

    # Демо поиска
    print("\n" + "=" * 60)
    print("Демонстрация поисковой системы")
    print("=" * 60)

    # Загрузка в поисковик
    search_engine = ClinicalSearchEngine()
    search_engine.chunks = [c.to_dict() for c in chunks]
    search_engine._indexed = True

    # Поиск по запросу
    print("\n[4] Поиск: 'тахикардия лечение'")
    results = search_engine.search("тахикардия лечение", top_k=3)
    print(search_engine.format_results(results))

    # Поиск по коду МКБ
    print("\n[5] Поиск по МКБ: I47.1")
    results = search_engine.search_by_icd("I47.1")
    print(search_engine.format_results(results))

    # Поиск препарата
    print("\n[6] Поиск препарата: 'верапамил'")
    results = search_engine.search_medications("верапамил")
    print(search_engine.format_results(results))

    print("\n" + "=" * 60)
    print("Демонстрация завершена!")
    print("=" * 60)


if __name__ == '__main__':
    main()
